// For Loop - Reverse
for (let i = 5; i >= 1; i--) {
    console.log("Reverse For:", i);
}

// While Loop - Reverse
let j = 5;
while (j >= 1) {
    console.log("Reverse While:", j);
    j--;
}

// Do While Loop - Reverse
let k = 5;
do {
    console.log("Reverse DoWhile:", k);
    k--;
} while (k >= 1);
