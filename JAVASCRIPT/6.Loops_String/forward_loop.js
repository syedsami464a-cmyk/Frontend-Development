// For Loop - Forward
for (let i = 1; i <= 5; i++) {
    console.log("Forward For:", i);
}

// While Loop - Forward
let j = 1;
while (j <= 5) {
    console.log("Forward While:", j);
    j++;
}

// Do While Loop - Forward
let k = 1;
do {
    console.log("Forward DoWhile:", k);
    k++;
} while (k <= 5);
